
public class processamento_Dados_Castig {

	public static void main(String[] args) {
		
		//SINTAXE:
		
		//<Variável> = <Expressão>;  o = lê se >>"recebe-se"
		
		int x, y;
		x = 5;
		y = 2*x;
		
		System.out.println(y);
		System.out.println(x);
		
		
		int z;
		double t;
		z = 5;
		t = 2*x;
		
		System.out.println(z);
		System.out.println(t);
		
		//Expressão de um Trapézio
		
		double b, B, h, area;
		b = 6.0;
		B = 8.0;
		h = 5.0;
		area = ((b+B)/2)*h;
		
		System.out.println(area);
		
		
		//casting conversão de valor de um tipo para o outro.
		int a, r;
		double resultado;
		
		a = 5;
		r = 2;
		
		resultado = (double) a/r;
		
		System.out.println(resultado);
	 
				
		
		


	}

}
